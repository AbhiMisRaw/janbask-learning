# '^' : matches begining of a line
# '$' : matches end of line
# '.' : matches any character
# '\s' : matched whitespace
# '\S' : matches any non-white character

# '*' : Zero or more time
# '*?' : Zero or more time (non-greedy)
# '+' : one or more time
# '+?' : one or more time (non-greedy)

# non-greedy means : It pick larger

# Example : "^A.*\sM.*" -> Abhishek Mishra
